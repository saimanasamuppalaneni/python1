# type casting in python
name="manasa"
age=12
isstudent=True
weight=41.3
print("type of name is ",type(name))
print("type of age is ",type(age))
print("type of isstudent is ",type(isstudent))
print("type of weight is ",type(weight))
age=str(age)
print(age)
print("type of age is ",type(age))
weight=int(weight)
print(weight)
print("type of weight is ",type(weight))